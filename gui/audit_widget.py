            if event[1] == 'recognition_attempt':
                # –°–æ–±—ã—Ç–∏—è —Ä–∞—Å–ø–æ–∑–Ω–∞–≤–∞–Ω–∏—è: –∑–µ–ª–µ–Ω—ã–π –¥–ª—è —É—Å–ø–µ—à–Ω—ã—Ö, –∫—Ä–∞—Å–Ω—ã–π –¥–ª—è –Ω–µ—É–¥–∞—á–Ω—ã—Ö
                tag = "success" if event[3] == 'success' else "failed"
